"""The reference agent reproduces the public conformance fixtures. This is the contract the
Rust `infertap` agent must also satisfy — same fixtures, same expected outputs. Fixtures live
in kv-policy-core/conformance/telemetry/ (generated by vllm_leg/conformance.py)."""

import json

import pytest

from agentic_kv_bench.vllm_leg.conformance import conformance_dir, run


def _fixtures():
    d = conformance_dir()
    return sorted(d.glob("*.json")) if d.exists() else []


@pytest.mark.parametrize("path", _fixtures(), ids=lambda p: p.stem)
def test_reference_reproduces_fixture(path):
    fx = json.loads(path.read_text())
    got = run(fx["config"], fx["events"])
    assert got == fx["expect"]


def test_fixtures_exist_and_are_versioned():
    files = _fixtures()
    if not files:
        pytest.skip("conformance fixtures not generated (run vllm_leg.conformance)")
    for path in files:
        fx = json.loads(path.read_text())
        assert fx["fixture_version"] == "v0"
        # portability guard: fixture block hashes must be ints (language-neutral salting)
        for ev in fx["events"]:
            if "block_hash" in ev:
                assert isinstance(ev["block_hash"], int)
